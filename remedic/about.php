      <span class="style1">The heart is a hollow muscle that pumps blood throughout the blood vessels by repeated, rhythmic contractions. It is found in all animals with a circulatory system (including all vertebrates).
The heart is the core of the circulatory system, which supplies the body with oxygen and other important nutrients needed to sustain life. Without it, one would couldn't exist. 
On average, the human heart weighs between six and eleven ounces and beats between 60 to 80 times per minute. It pumps about 2,000 gallons-enough to fill a small above-ground swimming pool-worth of blood through your body every day. 
Heart rate is affected by physical fitness, activity, emotions, and other factors. The healthier a heart is, the lower the resting heart rate will be. 
Hormones released because of emotions and other stimuli affect the heart rate, which is why the heart was historically associated with emotions. 
The heart has three layers. They are the:<br>
	Myocardium: The strongest of the three layers, this is the muscle that contracts to pump blood. <br>
	Epicardum: This thin membrane comprises the outermost layer of the heart.<br> 
	Endocardium: The innermost layer is thin and smooth.<br>
The heart is divided into four chambers-two atria and two ventricles. Blood is transported through the body via veins and arteries. 
The heart has a double-pump feature that transports blood away from it and back to it. Oxygenated blood leaves the left side of the heart through the ascending aorta. Blood flowing through the right side of the heart is returning from all over the body before it is sent to the lungs where it receives oxygen.
The vertebrate heart is principally composed of cardiac muscle and connective tissue. Cardiac muscle is an involuntary striated muscle tissue found only in this organ and responsible for the ability of the heart to pump blood.
The average human heart, beating at 72 beats per minute, will beat approximately 2.5 billion times during an average 66 year lifespan. It weighs approximately 250 to 300 grams (9 to 11 oz) in females and 300 to 350 grams (11 to 12 oz) in males. </span></p>  
